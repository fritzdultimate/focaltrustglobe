<div class="selections">
<div class="item">
<p class="item-title"><a href="#section1" class="viewer"><span></span>Lending</a></p>
<div class="desc" id="section1">
<p>Through the Fortressminers Credit team&rsquo;s extensive sourcing capabilities, corporate and asset based loans are directly originated to middle market companies in a broad array of industries and against numerous asset class. The Corporate &amp; Asset Based Loans team has the flexibility to structure and price debt across all levels of the capital structure including senior, unitranche, second lien and mezzanine, often with equity co-investments. The team employs a dynamic, multi-faceted underwriting approach which looks to underlying cash flows and enterprise value or to the value of tangible and intangible assets. The team employs this analytical approach in all situations including buyouts, refinancings, growth, rescue, and distressed environments.</p></div>
</div>
<div class="item">
<p class="item-title"><a href="#section2" class="viewer"><span></span>Corporate Debt & Securities</a></p>
<div class="desc" id="section2">
<p>The Corporate Securities team focuses on event-driven, idiosyncratic opportunities that exist in publicly traded securities across the capital structure. The team generally concentrates on out of favor industries where securities trade at stressed and distressed levels allowing for substantial upside. The investment model emphasizes deep industry and company-specific research as a backdrop to formulating opinions on intrinsic value, cash flow potential and eventual recoveries. The Corporate Securities team also emphasizes analysis of indentures and credit agreements and other relevant legal documents that may govern the securities in order to formulate further investments, including those surrounding litigation opportunities and inter-capital structure arbitrage.</p></div>
</div>
<div class="item">
<p class="item-title"><a href="#section3" class="viewer"><span></span>Portfolios & Orphaned Assets</a></p>
<div class="desc" id="section3">
<p>The Portfolio Acquisitions team invests in portfolios of performing and non-performing loans and assets in a variety of industries acquired from sellers including governmental organizations, banks and other financial organizations. The team is an active buyer of illiquid portfolios and has the ability to transact in size and on an expedited timeframe. The team also acts as a third party manager for large, liquidating, asset portfolios that involve organizational and asset-level complexity.</p></div>
</div>
<div class="item">
<p class="item-title"><a href="#section4" class="viewer"><span></span>Real Estate</a></p>
<div class="desc" id="section4">
<p>The Real Estate Loans &amp; Equity teams are focused on investing in distressed real estate loan acquisitions, special situation equity investments and recapitalizations, opportunistic lending, REIT debt and commercial real estate structured products. The teams have long standing distressed investing and asset management experience with a fully tested and integrated origination, acquisition, underwriting, asset and risk management infrastructure.</p></div>
</div>
<div class="item">
<p class="item-title"><a href="#section5" class="viewer"><span></span>Structured Finance</a></p>
<div class="desc" id="section5">
<p>The ABS & Residential Assets team has substantial experience investing in asset-backed and residential securities and loans. The team applies a rigorous bottom-up approach to analyzing the underlying borrowers and collateral of each asset, with an emphasis on fundamental value and principal preservation. The team utilizes intensive asset management and risk management techniques to maximize the value of its loan and security positions.</p></div>
</div>
