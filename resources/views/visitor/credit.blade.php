<div class="tab-overview-intro">
<p>Our Credit business is a leading, global alternative investor, managing assets on behalf of many of the world’s most prominent and sophisticated allocators, including public and private pensions, sovereign wealth funds, large institutions and private clients.</p>
<p>Our overriding objective is to protect and grow our investors’ capital, applying the discipline, rigor and expertise honed through decades of investment experience through a full range of market, economic and credit cycles and across asset classes, structures, geographies and industries.</p>
<p>Founded by Pete Briger in 2002, our Credit business today delivers local expertise with a global perspective in 11 office locations worldwide.  Managing Partners and co-Chief Investment Officers Pete Briger, Dean Dakolias, Drew McKnight and Joshua Pack, have worked together for nearly two decades, and today lead a team of approximately 525 professionals, including 130 professionals devoted to asset management.</p>
<p>We believe that the depth and breadth of our resources—our intellectual capital, investment, origination, asset management and servicing capabilities, and our time-tested and disciplined processes—distinguish and differentiate our businesses from the vast majority of market participants.</p>
