<div role="tabpanel" class="tab-pane fade" id="team-edge">
<p>The following factors differentiate the Fortressminers Credit team from other credit investors:</p>
<div class="panel-group" id="teamedge-accordion">
<div class="panel panel-default">
<div class="panel-heading"><h4 class="panel-title"><a role="button" data-toggle="collapse" data-target="#experience" aria-expanded="true"><span class="glyphicon"></span>Experience across credit cycles</a></h4></div>
<div id="experience" class="collapse show" data-parent="#teamedge-accordion">
<div class="tab-accordion-content">
<p>The Fortressminers Credit team has lengthy experience as credit investors. Senior credit team members were actively investing during the US Savings & Loan Crisis and in related RTC opportunities, and led major investment efforts in Asia during the Asian Financial Crisis. The team’s credit experience extends across all of our investment groups (ABS & Residential Assets, Corporate & Asset Based Loans, Corporate Securities, Portfolio Acquisitions, Real Estate Loans & Equity) creating valuable cross-class expertise. The Credit team’s deep, long term history as active credit investors through various types of investment environments has created a process and discipline which set the team apart from market participants who have only recently become interested in credit opportunities as a result of disruptions in global markets.</p>
</div>
</div>
</div>
<div class="panel panel-default">
<div class="panel-heading"><h4 class="panel-title"><a role="button" data-toggle="collapse" data-target="#flexibility" aria-expanded="false"><span class="glyphicon"></span>Investment flexibility</a></h4></div>
<div id="flexibility" class="collapse" data-parent="#teamedge-accordion">
<div class="tab-accordion-content">
<p>The Fortressminers Credit team pursues a broad, diversified strategy covering multiple asset classes and structures, supported by professionals in each of the primary investment groups. This allows the team to move in and out of sectors based on changing circumstances and to tackle opportunities requiring specialization in more than one investment category. This approach positions the Credit team to take advantage of investment opportunities that fall between the cracks of managers without the capability to underwrite diverse pools of credits.</p>
</div>
</div>
</div>
<div class="panel panel-default">
<div class="panel-heading"><h4 class="panel-title"><a role="button" data-target="#focus" data-toggle="collapse" aria-expanded="false"><span class="glyphicon"></span>Fundamental credit focus and structured finance approach</a></h4></div>
<div id="focus" class="collapse" data-parent="#teamedge-accordion">
<div class="tab-accordion-content">
<p>The Fortressminers Credit team is an active, fundamental value, asset based investor. The team is comfortable with, and to some degree specializes in, difficult credits, difficult borrowers and difficult situations. The Credit team is very focused on properly pricing, structuring, collateralizing, monitoring and, if necessary, litigating to maximize the value of credit investments.</p>
</div>
</div>
</div>
<div class="panel panel-default">
<div class="panel-heading"><h4 class="panel-title"><a role="button" data-target="#active-mgmt" data-parent="#teamedge-accordion" data-toggle="collapse" aria-expanded="false"><span class="glyphicon"></span>Active asset management</a></h4></div>
<div id="active-mgmt" class="collapse" data-parent="#teamedge-accordion" >
<div class="tab-accordion-content">
<p>The Fortressminers Credit team is extremely active in maximizing the value of investments through intensive asset management. The Credit team includes over 80 dedicated asset management professionals in eleven geographic locations. Certain transactions are economically compelling because returns will be generated through intensive servicing, such as in residential whole loans. The Fortressminers Credit team has been an active owner and operator in managing distressed assets and is fully capable and qualified to continue to manage and service complex assets.</p>
</div>
