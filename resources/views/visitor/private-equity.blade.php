<div role="tabpanel" class="tab-pane fade" id="investment-approach">
<p>Certain themes define the Private Equity Funds’ investment approach and illustrate how the team identifies, underwrites and creates value across many of {{ env('APP_NAME') }}'s private equity investments. These themes include:</p>
<div class="panel-group" id="approach-accordion" role="tablist">
<div class="panel panel-default">
<div class="panel-heading"><h4 class="panel-title"><a role="button" data-toggle="collapse" data-target="#assetfocus" aria-expanded="true"><span class="glyphicon"></span>Asset Focus</a></h4></div>
<div id="assetfocus" class="panel-collapse collapse show" data-parent="#approach-accordion">
<div class="tab-accordion-content">
<p>Across a broad spectrum of industries, the private equity team has significant experience investing in physical and financial assets ranging from railroads and airplanes, solar facilities and real estate, to loans and leases secured by commercial and consumer assets. In our view, asset-based investing provides a foundation of value against which investments can be underwritten and financed, and also facilitates a broader range of exit alternatives. Asset-based investments with stable long term cash flows can also provide an attractive option for taking advantage of positive economic trends while avoiding the “binary” outcomes linked to the vagaries of short-term economic events.</p>
</div>
</div>
</div>
<div class="panel panel-default">
<div class="panel-heading"><h4 class="panel-title"><a role="button" data-toggle="collapse" data-target="#valueinvesting" aria-expanded="false"><span class="glyphicon"></span>Value-oriented investing and tolerance for complexity</a></h4></div>
<div id="valueinvesting" class="panel-collapse collapse" data-parent="#approach-accordion">
<div class="tab-accordion-content">
<p>The {{ env('APP_NAME') }} Private Equity Funds look for investments that can be acquired at attractive valuations, often as a result of structural complexity, distress, or disfavor within the capital markets. In our experience, many of the best investments are made in difficult or changing environments, particularly when disruptions in the capital markets lead to a scarcity of funding for capital intensive businesses. This is true for whole company acquisitions as well as for opportunistic add-on investments within existing portfolio companies.</p>
<p>To secure the most compelling investment opportunities, the private equity team evaluates situations involving multiple asset classes, elaborate capital structures, and multifaceted tax and regulatory issues. {{ env('APP_NAME') }}’s investments are often at the nexus of private equity, distressed debt and asset-based investment disciplines, and require a multidisciplinary asset and corporate level approach to unlock value and optimize investment returns. In our experience, {{ env('APP_NAME') }}’s willingness to tackle complicated situations creates unique value buying opportunities.</p>
</div>
</div>
</div>
<div class="panel panel-default">
<div class="panel-heading"><h4 class="panel-title"><a role="button" data-target="#flexibleapproach" data-toggle="collapse" aria-expanded="false"><span class="glyphicon"></span>Flexible investment approach</a></h4></div>
<div id="flexibleapproach" class="panel-collapse collapse" data-parent="#approach-accordion">
<div class="tab-accordion-content">
<p>The {{ env('APP_NAME') }} Private Equity Funds take a flexible, opportunistic investment approach, and will pursue a broad range of transaction types and structures in order to secure the best investments. The Funds principally make direct control-oriented equity investments, but may also acquire assets or make debt investments, and, where appropriate, build portfolio companies to own and manage those assets. While primarily focused on companies with long term growth prospects, we will also buy runoff businesses and portfolios where cash flows are expected to be significant. In addition to public and private market buyout investments, we have completed significant acquisitions from governments seeking to privatize assets. In distressed situations, we will seek to secure corporate ownership or control by working with a company’s board of directors or creditor’s committee in order to guide in the restructuring or recapitalization process (including through bankruptcy restructuring). Finally, our investment program includes a significant focus on build-ups in consolidating industries.</p>
</div>
</div>
</div>
<div class="panel panel-default">
<div class="panel-heading"><h4 class="panel-title"><a role="button" data-target="#intensivemanagement" data-toggle="collapse" aria-expanded="false"><span class="glyphicon"></span>Intensive investment management</a></h4></div>
<div id="intensivemanagement" class="panel-collapse collapse" data-parent="#approach-accordion">
<div class="tab-accordion-content">
<p>The {{ env('APP_NAME') }} Private Equity Funds take an extremely active, hands-on approach to managing investments. {{ env('APP_NAME') }} relies on a team of over 60 investment professionals to work with our companies’ management teams on the operations, capitalization, capital deployment and strategic development of the portfolio investments. To assist portfolio company management through the evolution of their businesses, {{ env('APP_NAME') }} has created an Operational Support team made up of professionals with expertise in various functional areas such as: procurement services; risk management; accounting; internal and external financial reporting; financial planning and analysis; information technology; and control and compliance support.</p>
</div>
</div>
</div>
<div class="panel panel-default">
<div class="panel-heading"><h4 class="panel-title"><a data-target="#growthinvesting" role="button" data-toggle="collapse" aria-expanded="false"><span class="glyphicon"></span>Investing for growth</a></h4></div>
<div id="growthinvesting" class="panel-collapse collapse" data-parent="#approach-accordion">
<div class="tab-accordion-content">
<p>An important component of the {{ env('APP_NAME') }} Private Equity Funds’ investment approach is driving growth within portfolio companies by sourcing and executing follow-on investments. Even after a company’s day-to-day operations have been built out or restructured, and the management team solidified, {{ env('APP_NAME') }} Private Equity personnel remain significantly involved in major capital and investment decisions, and actively pursue incremental investment opportunities on behalf of and in partnership with the company’s management that the team believes will be accretive to its core operations. To raise growth capital for its private equity portfolio companies, the Private Equity Funds have significant experience accessing the public and private capital markets.</p>
</div>
</div>
